// classic stack implementation where we would perform the following operations:-
// Push(), pop(), peek() and getMin() functionalities implemented.

class MinStack {
    int min = Integer.MAX_VALUE; //Initialize min value to max initially.
    Stack<Integer> stack = new Stack<Integer>(); //Instantiate a stack.
    
    public void push(int x) {
        // only push the old minimum value when the current 
        // minimum value changes after pushing the new value x
        if(x <= min){          
            stack.push(min); //push min and update the min value...
            min=x;
        }
        stack.push(x);
    }

    public void pop() {
        // if pop operation could result in the changing of the current minimum value, 
        // pop twice and change the current minimum value to the last popped/minimum value.
        if(stack.pop() == min) min=stack.pop();
    }

    public int top() {
        return stack.peek();
    }

    public int getMin() {
        return min;
    }
}

